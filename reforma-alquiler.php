                                    <h3 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">Propietarios de inmuebles en CDMX: ¿Cómo protegerse ante las nuevas reformas en el mercado de alquiler?</h3>
                                    <p class="wow fadeInUp">La Ciudad de México cierra el 2024 con cambios significativos en las regulaciones de arrendamiento, que impactan directamente a los propietarios de inmuebles en renta. </p>
                                    <p class="wow fadeInUp">A continuación, te presentamos un análisis detallado sobre estas reformas y algunas estrategias que puedes implementar para proteger tu inversión.</p>
                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">Reformas recientes</h5>
                                    <p class="wow fadeInUp">En agosto de 2024, se aprobó en CDMX una reforma al Código Civil que limita el aumento en los precios de renta al nivel de la inflación anual anterior, limitando así los incrementos posibles y complicando la rentabilidad de las propiedades en alquiler. Adicionalmente, se estableció un nuevo registro digital de contratos de arrendamiento, el cual aún no cuenta con lineamientos claros para su implementación.
                                    </p>
                                    <p class="wow fadeInUp">Además, se introdujo una reforma a la Ley de Turismo que regula el mercado de alojamientos temporales (como los listados en plataformas tipo Airbnb). Ahora, los propietarios que deseen rentar sus inmuebles a corto plazo deberán registrarse en un padrón de anfitriones, cumplir con requisitos fiscales y normativas de Protección Civil, y mantener una ocupación máxima de 180 días al año, con el fin de proteger el mercado de vivienda residencial y equilibrar la competencia con el sector hotelero.
                                    </p>
                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">Nueva iniciativa contra "requisitos excesivos" en los contratos de arrendamiento</h5>
                                    <img src="images/requisitos.jpg" alt="requisitos" class="img-fluid" width="50%">
                                    <p class="wow fadeInUp">El pasado 29 de octubre, el diputado Alberto Vanegas Arenas (Morena) presentó una nueva iniciativa que busca reformar el artículo 2448-F del Código Civil de la CDMX, prohibiendo ciertos "requisitos excesivos" en los contratos de arrendamiento, con el objetivo de mejorar el acceso a una vivienda digna. Para propietarios, esta propuesta plantea desafíos importantes, pues restringe varias condiciones que tradicionalmente se utilizaban para protegerse ante riesgos financieros en el arrendamiento inmobiliario.
                                    </p>
                                    <p class="wow fadeInUp">Es por eso que te presentamos un análisis legal de los aspectos más destacados en esta iniciativa, elaborado por Rogelio Hernández, nuestro COO de Póliza de Rentas, una empresa líder en protección jurídica para arrendamientos inmobiliarios que cuenta con especialistas en más de 28 estados de la república Mexicana protegiendo el patrimonio de propietarios.
                                    </p>
                                    <p class="wow fadeInUp">A continuación, se señalan las prohibiciones que se plantean en la iniciativa contra los requisitos excesivos en contratos de arrendamiento, destacando, que esta propuesta sólo sería aplicable a la Ciudad de México:
                                    </p>
                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">1. Prohibición de exigir un "fiador" con inmueble de valor equivalente o superior al arrendado</h5>
                                    <p class="wow fadeInUp">Aunque la iniciativa menciona el término “aval,” la figura jurídica que legalmente reconoce el Código Civil para garantizar obligaciones civiles es la del “fiador", por lo que la redacción que propone el legislador podría resultar un poco imprecisa. En ese entendido, la propuesta prohíbe que el arrendador exija un fiador que respalde el cumplimiento de obligaciones con un inmueble cuyo valor sea igual o mayor al arrendado. Esto podría llevar a la necesidad de contar con avalúos de ambos inmuebles, para determinar cuál tendría el valor más elevado, lo cual complicaría y retrasaría el proceso del arrendamiento.
                                    </p>
                                    <p class="wow fadeInUp">Además, se pretende eliminar como requisito del contrato los datos del “fiador en garantía”, previstos actualmente en la fracción IX del artículo 2448-F, lo que podría traer como consecuencia que la figura del fiador, que actualmente es opcional, más allá de tomarse como una opción, a raíz de la reforma pueda interpretarse erróneamente como una prohibición de requerir "fiador". Esto limitaría la capacidad del arrendador para asegurar el cumplimiento de pago y podría aumentar el riesgo de incumplimiento contractual.
                                    </p>

                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">2. Prohibición de solicitar pagos anticipados de uno o más meses de renta</h5>
                                    <p class="wow fadeInUp">Esta disposición presenta una redacción ambigua que podría interpretarse como una prohibición de solicitar incluso el primer mes de renta por adelantado. En caso de aprobarse así, el arrendatario podría reclamar el derecho de ocupar el inmueble sin hacer un pago inicial, lo cual dejaría al arrendador sin ninguna garantía de pago, más que el depósito, en su caso. La falta de claridad en este punto podría afectar la seguridad financiera del arrendador, y será necesaria una aclaración a la redacción de la iniciativa para especificar si esta prohibición aplica solo a pagos anticipados posteriores al primer mes de renta.
                                    </p>
                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">3. Prohibición de requerir la firma de títulos de crédito en blanco</h5>
                                    <p class="wow fadeInUp">Aunque esta práctica es cuestionable y ha caído en desuso debido al sentido común y la cautela de los arrendatarios, su prohibición en la reforma refuerza la ética en los contratos. Sin embargo, es poco probable que este tipo de reforma tenga un impacto real debido a su baja incidencia en el mercado.
                                    </p>
                                    <p class="wow fadeInUp">No obstante, el hecho de que se esté considerando una prohibición de firmar títulos de crédito en blanco, no implica que no se puedan firmar títulos de crédito de manera alternativa para garantizar obligaciones de naturaleza civil. Al contrario, aunque dichos títulos son de naturaleza mercantil, como los son los pagarés, que se usan cotidianamente en la gestión de los arrendamientos inmobiliarios, es posible pactar entre las partes la firma de aquellos, siempre y cuando las partes estén plenamente conscientes de los alcances de su ejecución, ya que un pagaré, únicamente da derecho al acreedor al cobro de una cantidad líquida señalada en el título, más no por el hecho de que se pacte para garantizar obligaciones de un arrendamiento, dará derecho al cumplimiento de las obligaciones de pago de rentas y entrega del inmueble.
                                    </p>
                                    <h5 class="wow fadeInUp mt-5 mb20 color-dor" data-wow-delay=".2s">¿Cómo pueden protegerse los propietarios ante estas reformas?</h5>
                                    <p class="wow fadeInUp">Ante estos cambios, es normal que los propietarios se cuestionen cómo impactarán en sus ingresos y en el control sobre sus propiedades. Sin embargo, existen mecanismos para proteger su inversión sin contravenir la nueva normativa. Aquí algunos consejos clave:
                                    </p>
                                    <p class="wow fadeInUp">Conoce el perfil de tu inquilino: Obtener antecedentes y referencias del arrendatario puede ayudarte a evaluar su estabilidad económica y compromiso de pago. Es el primer paso para prevenir problemas a futuro.
                                    </p>
                                    <p class="wow fadeInUp">Opta por una póliza de arrendamiento: Las pólizas de arrendamiento ofrecen respaldo legal ante posibles incumplimientos e impagos, lo que brinda tranquilidad respecto a los ingresos del arrendador y el uso de su propiedad. Además, incluyen mecanismos preventivos de investigación de inquilinos, que ayudan a compensar la eliminación de garantías tradicionales como el fiador.
                                    </p>
                                    <p class="wow fadeInUp">Contratos bien estructurados: Redactar un contrato claro y conforme a la normativa vigente es esencial. Especificar los derechos y obligaciones de ambas partes, desde los términos de pago hasta las reglas de uso, previene malentendidos y refuerza la seguridad jurídica de la relación arrendador-arrendatario.
                                    </p>
                                    <p class="wow fadeInUp">Asesoría legal especializada: Contar con el apoyo de un abogado o asesor especializado en arrendamientos puede ayudarte a estructurar contratos acordes a las reformas y a adaptar tus prácticas para proteger tu inversión de forma segura y acorde a la ley.
                                    </p>
